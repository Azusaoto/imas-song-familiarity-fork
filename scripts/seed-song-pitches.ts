async function main() {
  console.log('--- 開始更新歌曲音域 ---');
  let updateCount = 0;

  const entries = Object.entries(songPitchMap);
  const total = entries.filter(([_, p]) => p.lowestPitch.trim() !== '' || p.highestPitch.trim() !== '').length;
  console.log(`共有 ${total} 首歌曲填寫了音域資料，開始寫入資料庫...`);

  for (const [title, pitches] of entries) {
    const lowest = pitches.lowestPitch.trim() !== '' ? pitches.lowestPitch.trim() : null;
    const highest = pitches.highestPitch.trim() !== '' ? pitches.highestPitch.trim() : null;

    if (lowest === null && highest === null) {
      continue; // 略過未填寫音域的歌曲
    }

    try {
      const result = await prisma.song.updateMany({
        where: {
          title: title,
        },
        data: {
          lowestPitch: lowest,
          highestPitch: highest,
        },
      });

      if (result.count > 0) {
        console.log(`[更新成功] 歌曲: 「${title}」 -> 最低音: ${lowest || '--'}, 最高音: ${highest || '--'} (${result.count} 首)`);
        updateCount += result.count;
      } else {
        console.warn(`[找不到歌曲] 資料庫中找不到曲名為 「${title}」 的歌曲。`);
      }
    } catch (error: any) {
      console.error(`[更新失敗] 歌曲: 「${title}」 寫入錯誤: ${error.message}`);
    }
  }

  console.log(`\n--- 音域更新完畢！共更新了 ${updateCount} 筆音域記錄 ---`);
}

main()
  .catch((e) => {
    console.error('執行 Seed 腳本時發生錯誤:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
